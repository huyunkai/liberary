	private void jButton_sureActionPerformed(java.awt.event.ActionEvent evt) {
		if (jTextField_user.getText() == null || jTextField_user.getText().length() < 1)
		{
			JOptionPane.showMessageDialog(this,"请输入学号！","错误",JOptionPane.INFORMATION_MESSAGE);
			return;
		}
		if (jTextField_ISBN.getText() == null || jTextField_ISBN.getText().length() < 1)
		{
			JOptionPane.showMessageDialog(this,"请输入ISBN号！","错误",JOptionPane.INFORMATION_MESSAGE);
			return;
		}
		Vector<String> rec = new Vector<String>();
		rec.add(jTextField_user.getText());
		rec.add(jTextField_ISBN.getText());
		Calendar now = Calendar.getInstance();
		//rec.add();rec.add("1");
		now.add(Calendar.MONTH,1);//这个时间我们搞定啦的 ？？什么意思？不是java系统全部由0开始的嘛  我们后面试了多次  看在那加1 测试这个 就行了
		// 那你们现在还是用的我这里写的这样吗？我们只是增加了1 2行代码而已  坚决那个时间差1个月而已 其余的都没改哦。
		rec.add(now.get(now.YEAR) + "-" + now.get(Integer.valueOf(now.MONTH)) + "-" + now.get(now.DAY_OF_MONTH));
		now.add(Calendar.MONTH,2);
		rec.add(now.get(now.YEAR) + "-" + now.get(Integer.valueOf(now.MONTH)) + "-" + now.get(now.DAY_OF_MONTH));
		try {
			bao.add(rec);
		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

	private void jButton_inquireActionPerformed(java.awt.event.ActionEvent evt) {
		Vector<String> rec;
		try {
			rec = dao.getById(jTextField_ISBN.getText());
			if (rec == null || rec.size() < 1) {
				JOptionPane.showMessageDialog(this, "没有这本书！", "错误",
						JOptionPane.INFORMATION_MESSAGE);
				return;
			}
			jTextField_bookName.setText(rec.get(2));
			jTextField_press.setText(rec.get(3));
			jTextField_author.setText(rec.get(4));
			jTextField_category.setText(rec.get(6));
		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

