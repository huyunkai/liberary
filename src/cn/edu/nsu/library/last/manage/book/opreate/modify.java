		record.add(jTextField_isbn.getText());
		record.add(jTextField_bookName.getText());
		record.add(jTextField_author.getText());
		record.add(jTextField_price.getText());
		record.add(jTextField_press.getText());
		record.add(jTextField_introduction.getText());
