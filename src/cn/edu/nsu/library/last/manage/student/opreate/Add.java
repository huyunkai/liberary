	private void jButton_sureActionPerformed(java.awt.event.ActionEvent evt) {
		Vector<String> record = new Vector<String>();

		record.add(jTextField_id.getText());
		record.add(jTextField_name.getText());
		record.add(jTextField_sex.getText());
		record.add(jTextField_dep.getText());
		record.add(jTextField_class.getText());
		record.add(jTextField_title.getText());
		record.add(jTextField_phone.getText());
		//将这些数据添加到DB中
		try {
			dao.add(record);
		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		jLabel_message.setText("添加成功");
	}

