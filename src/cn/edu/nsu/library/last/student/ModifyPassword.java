	private void jButton_sureActionPerformed(java.awt.event.ActionEvent evt) {
		
		if (String.valueOf(jPasswordField_newPassword.getPassword()).equals(
				String.valueOf(jPasswordField_password.getPassword()))) {
			Vector<String> record = new Vector<String>();
			record.add(String.valueOf(jPasswordField_password.getPassword()));

			try {
				System.out.println(StudentLogin.loginName);
				dao.modifyPassword(record, StudentLogin.loginName);
			} catch (ClassNotFoundException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			} catch (SQLException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
			jLabel_message.setText("修改成功！");
		} else {
			jLabel_message.setText("两次密码输入不一致！！");
		}

	}

